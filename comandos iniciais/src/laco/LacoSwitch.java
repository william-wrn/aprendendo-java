package laco;

public class LacoSwitch {
    public static void main(String[] args) {
        int valor = 5;

        switch (valor){
            case 1:
                System.out.println("Escolheu 1");
                break;
            case 2:
                System.out.println("Escolheu 2");
                break;
            case 3:
                System.out.println("Escolheu 3");
                break;
            default:
                System.out.println("Outra opção");
        }
    }
}
